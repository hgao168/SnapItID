// Look up a translation key for a given language. Falls back to English then
// to the raw key so missing keys are still visible (not silent).
export function tr(lang, key) {
  const table = UI_TEXT[lang] || UI_TEXT.en;
  return table[key] || UI_TEXT.en[key] || key;
}

// Same as tr() with simple {placeholder} substitution.
export function trf(lang, key, replacements) {
  let text = tr(lang, key);
  Object.keys(replacements || {}).forEach((k) => {
    text = text.replaceAll(`{${k}}`, replacements[k]);
  });
  return text;
}
